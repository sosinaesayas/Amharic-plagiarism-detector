sfx_arr = [sera_transliterate(suffix, "am") for suffix in suffix_list.split("|")]
sfx_arr.append("Wa")  # Special case for ሯ

pfx_arr = [sera_transliterate(prefix, "am") for prefix in prefix_list.split("|")]

def stem(word):
    cv_string = sera_transliterate(word, "am")

    # Remove suffixes
    for sfx in sfx_arr:
        if cv_string.endswith(sfx):
            cv_string = re.sub(f'{sfx}$', '', cv_string)
            break

    # Remove prefixes
    for pfx in pfx_arr:
        if cv_string.startswith(pfx):
            cv_string = re.sub(f'^{pfx}', '', cv_string)
            break

    # Remove infixes
    if re.search(r'.+([^aeiou])[aeiou]\1[aeiou].?', cv_string, re.IGNORECASE):
        cv_string = re.sub(r'\S\S[^aeiou][aeiou]', cv_string[0] + cv_string[1], cv_string, 1, re.IGNORECASE)
    elif re.search(r'^(.+)a\1$', cv_string, re.IGNORECASE):
        cv_string = re.sub(r'a.+', '', cv_string, 1, re.IGNORECASE)

    if re.search(r'[bcdfghjklmnpqrstvwxyz]{2}e', cv_string, re.IGNORECASE):
        ccv = re.search(r'[bcdfghjklmnpqrstvwxyz]{2}e', cv_string, re.IGNORECASE)
        if ccv:
            cv_string = re.sub(r'[bcdfghjklmnpqrstvwxyz]{2}e', ccv.group(0)[0] + "X" + ccv.group(0)[1], cv_string, 1, re.IGNORECASE)

    return sera_transliterate(cv_string, "en")
